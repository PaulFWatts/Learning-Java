/**
 * Sample code tracing problem.
 * @author James Montgomery
 */
public class LetsTraceSomeCode {

    public static void main(String[] args) {
        int a, b, c;
        
        a = 5;
        b = 12;
        System.out.println("b was " + b);
        c = b / a;
        b = c + a;
        System.out.println("b is now " + b);
    }
    
}